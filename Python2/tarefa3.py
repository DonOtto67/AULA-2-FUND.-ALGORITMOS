dia = int(input("Digite um número de 1 a 7: "))
if dia == 1:
    print("DOMINGO")
elif dia == 2:
    print("SEGUNDA")
elif dia == 3:
    print("TERÇA")
elif dia == 4:
    print("QUARTA")
elif dia == 5:
    print("QUINTA")
elif dia == 6:
    print("SEXTOU PAE")
elif dia == 7:
    print("SABADO")
else:
    print("NUMERO ERRADO PAE")